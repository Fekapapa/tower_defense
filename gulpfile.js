// ///////////////////////////////////////////////
// Required
// ///////////////////////////////////////////////

var gulp = require('gulp'),
    uglify = require('gulp-uglify'),
    rename = require('gulp-rename');

// ///////////////////////////////////////////////
// Scripts Tasks
// ///////////////////////////////////////////////

gulp.task('scripts', function() {
  gulp.src(['javascript/game_controller.js'])
    .pipe(rename({prefix:'xp_webtech_krf_', suffix:'.min'}))
    .pipe(uglify())
    .pipe(gulp.dest('build'));
});




// ///////////////////////////////////////////////
// Compass / Sass Tasks
// ///////////////////////////////////////////////







// ///////////////////////////////////////////////
// Watch Tasks
// ///////////////////////////////////////////////

gulp.task('watch', function() {
  gulp.watch('javascript/game_controller.js', ['scipts']);
});
